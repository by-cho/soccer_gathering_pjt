class Feed {
  String tag;
  String title;
  String place;
  String time;
  String userName;
  String profilePic;
  String date;
  String likeNum;
  String replyNum;
  String contents;

  Feed({
    required this.tag,
    required this.title,
    required this.place,
    required this.time,
    required this.userName,
    required this.profilePic,
    required this.date,
    required this.likeNum,
    required this.replyNum,
    required this.contents,
  });
}
